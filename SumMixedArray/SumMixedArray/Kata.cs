using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SumMixedArray
{
    public static int SumMix(object[] x)
    {
        int sum = 0;
        foreach (object obj in x)
        {
            int number = (int)obj;
            sum += number 
        }
    }
}
